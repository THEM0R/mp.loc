ШТАХЕТНИК Паркан є невіддільною частиною приватної оселі. Досить важко знайти приватну нерухомість, що не мала би паркану чи іншої огорожі. Окрім суто охоронної функції, паркан є важливим декоративним архітектурним елементом. Людей зустрічають по одежі, а будинок по огорожі. В останній час особливо популярним стає встановлення парканів та огорож з металевого штахетника. Цьому сприяє не висока ціна металевих штахет та їх практичність .

